const tables: Record<Language, Dictionary> = { en, sr };

export const t = (language: Language, key: string): string => tables[language][key] ?? key;

export const localizeGoal = (language: Language, goal: Goal) => {
  const map: Record<Goal, string> = {
    lose: t(language, "loseWeight"),
    maintain: t(language, "maintainWeight"),
    gain: t(language, "gainWeight"),
  };
  return map[goal];
};

export const localizeActivity = (language: Language, value: ActivityLevel) => {
  const map: Record<ActivityLevel, string> = {
    sedentary: t(language, "sedentary"),
    light: t(language, "lightActive"),
    moderate: t(language, "moderateActive"),
    active: t(language, "active"),
    veryActive: t(language, "veryActive"),
  };
  return map[value];
};

export const localizeMealCategory = (language: Language, value: MealCategory) => t(language, value);
